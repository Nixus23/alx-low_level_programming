#include "main.h"
/**
 * print_square - function prints a square
 * @size: function takes an integer parameter size
 * Return: function has a void return
 */
void print_square(int size)
{
	int i, j;

	if (size > 0)
	{
		for (i = 0; i < size; i++)
		{
			for (j = 0; j < size; j++)
			{
				_putchar('#');
			}
			_putchar('\n');
		}
	}
	else
		_putchar('\n');
}
